# Recursive divide-and-conquer matrix multiplication.
#
# Canonical (TooManyCooks) implementation:
# ../../cpp/TooManyCooks/matmul.cpp
# base case: ../../cpp/2common/matmul.hpp
#
# The matrices are split into quadrants recursively down to a 32x32 base case.
# The 8 sub-multiplications are run as two sequential groups of 4 parallel
# Weave tasks (spawn) so that no output tile is written by two tasks at once.
#
# A, B and C share three flat backing seqs owned by the root; each `Mat` value
# carries raw UncheckedArray pointers, base offsets (ao/bo/co) and the
# full-matrix stride (nn) into them. The parallel tasks in a group write
# disjoint sub-tiles of C, so no locking is required, and passing raw pointers
# keeps GC-managed memory out of the Weave tasks.
#
# The explicit {.gcsafe.} is required on Nim 2.x: gcsafety is not inferred for
# recursive procs, and the task wrapper generated by Weave's spawn demands it.

import std/[monotimes, os, strutils, times]
import weave
import benchutil

type Mat = object
  a, b, c: ptr UncheckedArray[int32]
  ao, bo, co: int # base offsets into a/b/c
  n, nn: int      # submatrix size, and stride (the full matrix dimension N)

func sub(m: Mat, ao, bo, co: int): Mat =
  Mat(a: m.a, b: m.b, c: m.c, ao: ao, bo: bo, co: co, n: m.n div 2, nn: m.nn)

proc matmulSmall(m: Mat) =
  for i in 0 ..< m.n:
    for k in 0 ..< m.n:
      for j in 0 ..< m.n:
        m.c[m.co + i * m.nn + j] +=
          m.a[m.ao + i * m.nn + k] * m.b[m.bo + k * m.nn + j]

proc matmul(m: Mat): bool {.gcsafe.} =
  if m.n <= 32:
    matmulSmall(m)
    return true
  let k = m.n div 2
  let nn = m.nn
  let (ao, bo, co) = (m.ao, m.bo, m.co)

  let groups = [
    # Group 1: the four products that only touch the "upper-left" halves.
    [m.sub(ao, bo, co),
     m.sub(ao, bo + k, co + k),
     m.sub(ao + k * nn, bo, co + k * nn),
     m.sub(ao + k * nn, bo + k, co + k * nn + k)],
    # Group 2: the accumulating products into the same tiles.
    [m.sub(ao + k, bo + k * nn, co),
     m.sub(ao + k, bo + k * nn + k, co + k),
     m.sub(ao + k * nn + k, bo + k * nn, co + k * nn),
     m.sub(ao + k * nn + k, bo + k * nn + k, co + k * nn + k)]]

  for g in groups:
    var children: array[4, Flowvar[bool]]
    for i in 0 ..< 4:
      children[i] = spawn matmul(g[i])
    for i in 0 ..< 4:
      discard sync(children[i])
  result = true

proc runMatmul(n: int): seq[int32] =
  var a = newSeq[int32](n * n)
  var b = newSeq[int32](n * n)
  var c = newSeq[int32](n * n)
  for i in 0 ..< n * n:
    a[i] = 1
    b[i] = 1
  discard matmul(Mat(
    a: cast[ptr UncheckedArray[int32]](addr a[0]),
    b: cast[ptr UncheckedArray[int32]](addr b[0]),
    c: cast[ptr UncheckedArray[int32]](addr c[0]),
    ao: 0, bo: 0, co: 0, n: n, nn: n))
  result = c

proc validate(c: seq[int32], n: int) =
  for i in 0 ..< n:
    for j in 0 ..< n:
      let res = c[i * n + j]
      if res != int32(n):
        echo "Wrong result at (", i, ",", j, ") : ", res, ". expected ", n
        quit(1)

proc main() =
  if paramCount() < 1:
    echo "Usage: matmul <matrix size (power of 2)>"
    return
  var n: int
  try:
    n = parseInt(paramStr(1))
  except ValueError:
    echo "Usage: matmul <matrix size (power of 2)>"
    return
  let threadCount = threadCountArg(2)
  echo "threads: ", threadCount

  setWeaveThreads(threadCount)
  init(Weave)

  discard runMatmul(n) # warmup

  echo "runs:"
  let start = getMonoTime()
  let res = runMatmul(n)
  let durUs = (getMonoTime() - start).inMicroseconds
  exit(Weave)
  validate(res, n)

  echo "  - matrix_size: ", n
  echo "    duration: ", durUs, " us"
  echo "    max_rss: ", peakMemoryUsageKiB(), " KiB"

when isMainModule:
  main()
