#!/usr/bin/env bash
# Compiles every Java stdlib (virtual-thread) benchmark into build/classes and
# generates a launcher script at build/<benchmark> for each, so that
# build_and_bench_all.py can invoke build/<benchmark> exactly like the native
# (CMake) and Go/Rust executables, matching the build/<benchmark> convention.
#
# The first argument (a CMake preset name on macOS/Windows) is ignored; javac
# does not use presets.
#
# RUNTIME_BENCHMARKS_LIBRARY_REF (used by the single-runtime / compare modes to
# pin a library version) is intentionally ignored here: the "library" under test
# is the Java standard library, whose version is the JDK's. Select a different
# JDK (e.g. via JAVA_HOME) to compare stdlib versions.
set -euo pipefail
cd "$(dirname "$0")"

# benchmark name -> fully qualified main class
BENCHMARKS=(skynet fib nqueens matmul channel io_socket_st)
CLASSES=(bench.Skynet bench.Fib bench.Nqueens bench.Matmul bench.Channel bench.IoSocketSt)

rm -rf build
mkdir -p build/classes

# Compile every source under src/ into build/classes.
find src -name '*.java' -print0 | xargs -0 javac -d build/classes

# Emit a launcher script named exactly after each benchmark so the harness finds
# build/<benchmark>. Each launcher resolves its own directory so it works no
# matter what CWD the harness invokes it from.
for i in "${!BENCHMARKS[@]}"; do
  name="${BENCHMARKS[$i]}"
  class="${CLASSES[$i]}"
  exe="build/${name}"
  cat > "$exe" <<EOF
#!/usr/bin/env bash
# Auto-generated by build_all.sh — launches the ${name} benchmark on the JVM.
dir="\$(cd "\$(dirname "\$0")" && pwd)"
exec java -cp "\$dir/classes" ${class} "\$@"
EOF
  chmod +x "$exe"
done
