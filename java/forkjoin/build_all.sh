#!/usr/bin/env bash
# Compiles every Java ForkJoinPool benchmark into build/classes and generates a
# launcher script at build/<benchmark> for each, so that build_and_bench_all.py
# can invoke build/<benchmark> exactly like the native (CMake) and Go/Rust
# executables, matching the build/<benchmark> convention.
#
# This runtime only implements the four recursive fork-join benchmarks: the
# ForkJoinPool RecursiveTask/RecursiveAction model is a compute scheduler, so it
# deliberately skips io_socket_st and channel (the I/O and MPMC-queue benchmarks)
# -- exactly as the pure fork-join C++ libraries (libfork, tbb, taskflow, coros)
# do. Use the java/std (Virtual Threads) runtime for those.
#
# The first argument (a CMake preset name on macOS/Windows) is ignored; javac
# does not use presets. RUNTIME_BENCHMARKS_LIBRARY_REF is also ignored: the
# "library" under test is the Java standard library, whose version is the JDK's.
set -euo pipefail
cd "$(dirname "$0")"

# benchmark name -> fully qualified main class
BENCHMARKS=(skynet fib nqueens matmul)
CLASSES=(bench.Skynet bench.Fib bench.Nqueens bench.Matmul)

rm -rf build
mkdir -p build/classes

# Compile every source under src/ into build/classes.
find src -name '*.java' -print0 | xargs -0 javac -d build/classes

# Emit a launcher script named exactly after each benchmark so the harness finds
# build/<benchmark>. Each launcher resolves its own directory so it works no
# matter what CWD the harness invokes it from.
for i in "${!BENCHMARKS[@]}"; do
  name="${BENCHMARKS[$i]}"
  class="${CLASSES[$i]}"
  exe="build/${name}"
  cat > "$exe" <<EOF
#!/usr/bin/env bash
# Auto-generated by build_all.sh — launches the ${name} benchmark on the JVM.
dir="\$(cd "\$(dirname "\$0")" && pwd)"
exec java -cp "\$dir/classes" ${class} "\$@"
EOF
  chmod +x "$exe"
done
